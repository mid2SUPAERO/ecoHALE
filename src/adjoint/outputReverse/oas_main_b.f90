    real(kind=8) :: nodes2b(n, 3), r2b(n-1), disp2b(n, 6), p1p0b(3)
    real(kind=8) :: tempb6
    real(kind=8) :: tempb5
    real(kind=8) :: tempb4
    real(kind=8) :: tempb3
    real(kind=8) :: tempb2
    real(kind=8) :: tempb1
    real(kind=8) :: tempb0
    real(kind=8) :: tempb
    real(kind=8) :: temp
    nodes2 = nodes
    r2 = r
