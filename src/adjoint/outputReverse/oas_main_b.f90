    end if
    moment(:, :) = 0.
    do j=1,ny-1
      do i=1,nx-1
        arg1(:) = (bpts(i, j+1, :)+bpts(i, j, :))/2. - cg
        call cross(arg1(:), sec_forces(i, j, :), tmp)
        moment(j, :) = moment(j, :) + tmp
