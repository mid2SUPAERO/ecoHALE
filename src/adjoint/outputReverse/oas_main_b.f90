    implicit none
! input
    integer, intent(in) :: n
    real(kind=8), intent(in) :: nodes(n, 3), r(n-1), disp(n, 6)
    real(kind=8) :: nodesb(n, 3), rb(n-1), dispb(n, 6)
    real(kind=8), intent(in) :: e, g, x_gl(3)
    real(kind=8) :: x_glb(3)
! output
    real(kind=8) :: vonmises(n-1, 2)
    real(kind=8) :: vonmisesb(n-1, 2)
