    real(kind=8) :: vb(3)
    real(kind=8) :: u(3)
    real(kind=8) :: ub(3)
    real(kind=8) :: nm
    real(kind=8) :: nmb
    call norm(v, nm)
    vb(3) = vb(3) + ub(3)/nm
    nmb = -(v(3)*ub(3)/nm**2)
    ub(3) = 0.0_8
    vb(2) = vb(2) + ub(2)/nm
    nmb = nmb - v(2)*ub(2)/nm**2
    ub(2) = 0.0_8
    vb(1) = vb(1) + ub(1)/nm
    nmb = nmb - v(1)*ub(1)/nm**2
    ub(1) = 0.0_8
    call norm_b(v, vb, nm, nmb)
  end subroutine unit_b
